public class MathOperation {

    public static double summ(double a,double b){

        return a+b;

    }
    public static double diff(double a,double b){

        return a-b;
    }
    public static double mul(double a,double b){

        return a*b;

    }
    public static double div(double a,double b){

       return a/b;
    }
}
